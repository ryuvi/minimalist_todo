import { MD3LightTheme } from "react-native-paper";

const baseLightThemeProps = {
  ...MD3LightTheme,
};

const errorColors = {
  error: "#F4B2B2",
  onError: "#4B2020",
  errorContainer: "#FDE1E1",
  onErrorContainer: "#2C1B1B",
};

const createLightTheme = (customColors: Object) => {
  return {
    ...baseLightThemeProps,
    colors: {
      ...MD3LightTheme.colors,
      ...customColors,
      ...errorColors,
      shadow: '#000000',
      scrim: '#000000',
    },
  };
};

